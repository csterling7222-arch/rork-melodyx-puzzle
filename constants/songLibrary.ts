export function getSongsByEra(era: string): SongMetadata[] {
  return EXPANDED_SONG_LIBRARY.filter(s => s.era === era);
}

export function getSongsByGenre(genre: string): SongMetadata[] {
  return EXPANDED_SONG_LIBRARY.filter(s => s.genre === genre);
}

export function getSongsByTag(tag: string): SongMetadata[] {
  return EXPANDED_SONG_LIBRARY.filter(s => s.tags.includes(tag));
}

export function getSongsByDifficulty(difficulty: SongMetadata['difficulty']): SongMetadata[] {
  return EXPANDED_SONG_LIBRARY.filter(s => s.difficulty === difficulty);
}

export function getRandomSong(excludeIds: string[] = []): SongMetadata {
  const available = EXPANDED_SONG_LIBRARY.filter(s => !excludeIds.includes(s.id));
  return available[Math.floor(Math.random() * available.length)];
}

export function getDailySongFromLibrary(): SongMetadata {
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
  const index = seed % EXPANDED_SONG_LIBRARY.length;
  return EXPANDED_SONG_LIBRARY[index];
}

export function getArtistInfo(artistName: string): ArtistInfo | null {
  return ARTIST_DATABASE[artistName] ?? null;
}

export function searchSongs(query: string): SongMetadata[] {
  const q = query.toLowerCase();
  return EXPANDED_SONG_LIBRARY.filter(s => 
    s.name.toLowerCase().includes(q) ||
    s.artist.toLowerCase().includes(q) ||
    s.genre.toLowerCase().includes(q) ||
    s.tags.some(t => t.toLowerCase().includes(q))
  );
}
